    void atualizar() {
        validaNumero(jTextField1);
        salvando = false;
        try {
            principal p = new principal();
            String sql = "SELECT cracha,nome,setor,funcao,armario,date_format(datacad,'%d/%m/%Y'),sexo,telefone FROM colaborador WHERE cracha=" + p.ob2;
            con = conexao.conectar();
            stm = con.createStatement();
            rs = stm.executeQuery(sql);
            jButton3.setEnabled(false);
            while (rs.next()) {
                jButton3.setEnabled(true);
                jButton1.setEnabled(false);
                jTextField1.setText(String.valueOf(rs.getInt("cracha")));
                jTextField2.setText(rs.getString("nome"));
                jTextField3.setText(rs.getString("setor"));
                jTextField4.setText(rs.getString("funcao"));
                jTextField5.setText(rs.getString("telefone"));
                jTextField6.setText(String.valueOf(rs.getObject("date_format(datacad,'%d/%m/%Y')")));
                if (rs.getString("sexo").equals("MASCULINO")) {
                    jComboBox1.setSelectedIndex(0);
                } else {
                    jComboBox1.setSelectedIndex(1);
                }
            }
        } catch (SQLException e) {
            System.out.println("Erro:" + e.getMessage());
        }
    }

    public void validaNumero(JTextField Numero) {
        long valor;
        if (Numero.getText().length() != 0) {
            try {
                valor = Long.parseLong(Numero.getText());
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Este campo só aceita caractere númerico", "Informação", JOptionPane.INFORMATION_MESSAGE);
                Numero.grabFocus();
                jTextField1.requestFocus();
            }
        }
    }

