    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        jTextField2.setEditable(true);
        jTextField3.setEditable(true);
        jTextField4.setEditable(true);
        jTextField5.setEditable(true);
        jTextField2.setFocusable(true);
        jTextField3.setFocusable(true);
        jTextField4.setFocusable(true);
        jTextField5.setFocusable(true);
        jComboBox1.setEnabled(true);
        jButton1.setEnabled(true);
        jTextField2.requestFocus();
        if (jButton3.getText().equals("Deletar")) {
            int op;
            op = JOptionPane.showConfirmDialog(rootPane, "Atenção, os dados serão excluídos?", "Confirmação", 2, JOptionPane.OK_CANCEL_OPTION);
            if (op == 0) {
                try {
                    String sql = "SELECT ARMARIO FROM COLABORADOR WHERE CRACHA ="+jTextField1.getText();
                    con = conexao.conectar();
                    stm = con.createStatement();
                    rs = stm.executeQuery(sql);
                    while(rs.next()){
                    if (rs.getInt("armario") == 0) {
                        sql = "DELETE FROM COLABORADOR WHERE CRACHA ="+jTextField1.getText();
                        con = conexao.conectar();
                        stm = con.createStatement();
                        stm.executeUpdate(sql);
                        JOptionPane.showMessageDialog(rootPane, "Colaborador excluído com sucesso!");
                        p.atualizarTable();
                        this.dispose();
                    }else
                        JOptionPane.showMessageDialog(rootPane, "Colaborador possui armario, remova-o antes de deleter seus dados!", "Erro", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(rootPane, "Erro ao excluir colaborador: "+e.getMessage(),"Erro", JOptionPane.ERROR_MESSAGE);
                }
                
            }else{
                jTextField2.requestFocus();
            }
        } else {
            jButton3.setText("Deletar");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

